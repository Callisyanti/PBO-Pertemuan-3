public class BangunRuang{
    private int panjang;
    private int lebar;
    private int tinggi;

     // constructor
     public BangunRuang() {
    }// end constructor
     // mutator/setter methods

     public void setpanjang(int p) {
        panjang = p;
    }

    public void setlebar(int l) {
        lebar = l;
    }

    public void settinggi(int t) {
        tinggi = t;
    }

    public int getpanjang() {
        return panjang;
    }// end method getpanjang

    public int gettinggi() {
        return tinggi;
    }// end method gettinggi

    public int getlebar() {
        return lebar;
    }// end method getlebar


}